    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
         String getBil1 = teBilPertama.getText();
        String getBil2 = teBilKedua.getText();
        bilPertama = Integer.parseInt(getBil1);
        bilKedua = Integer.parseInt(getBil2);
        
        if (instansi == "KPK") {
            hasilKPK(bilPertama, bilKedua);
        } else {
            hasilFPK(bilPertama, bilKedua);
        }
        
        taHasil.setText(Integer.toString(bilHasil));
    }//GEN-LAST:event_btnHitungActionPerformed

