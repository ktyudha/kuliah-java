    private void cbInstansiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbInstansiActionPerformed
        // TODO add your handling code here:
       
        if (cbInstansi.getSelectedItem().equals("KPK")) {
            instansi = "KPK";
        } else if (cbInstansi.getSelectedItem().equals("FPK")) {
            instansi = "FPK";
        }
    }//GEN-LAST:event_cbInstansiActionPerformed

    public void hasilKPK (int awal, int akhir) {
        bilHasil = awal * akhir;
    }
    
    public void hasilFPK (int awal, int akhir) {
        bilHasil = awal + akhir;
    }
    
